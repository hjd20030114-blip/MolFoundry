def _has_module(name: str) -> bool:
    try:
        __import__(name)
        return True
    except Exception:
        return False

HAS_RDKIT = _has_module("rdkit")

try:
    if HAS_RDKIT:
        from rdkit import Chem
        from rdkit.Chem import AllChem
        from rdkit.Chem import rdMolTransforms
        from rdkit.Chem.rdchem import Mol
        from rdkit.Chem.rdMolDescriptors import CalcNumRings
        from rdkit.Chem.rdchem import GetPeriodicTable
except Exception:
    HAS_RDKIT = False

HAS_PSI4 = _has_module("psi4")
HAS_NUMPY = _has_module("numpy")

if HAS_NUMPY:
    import numpy as np

# xTB CLI 探测
XTB_EXE = shutil.which("xtb")
HAS_XTB = XTB_EXE is not None

HARTREE_TO_EV = 27.211386245988

# -------------------------
# RDKit 构象与XYZ导出
# -------------------------

def smiles_to_3d_mol(smiles: str, max_attempts: int = 50, seed: int = 42) -> Optional["Mol"]:
    """将SMILES转3D构象并进行MMFF优化；失败返回None。"""
    if not HAS_RDKIT:
        return None
    try:
        mol = Chem.MolFromSmiles(smiles)
        if mol is None:
            return None
        mol = Chem.AddHs(mol)
        params = AllChem.ETKDGv3()
        params.randomSeed = seed
        params.useSmallRingTorsions = True
        params.pruneRmsThresh = 0.1
        if AllChem.EmbedMolecule(mol, params) != 0:
            # 重试使用不同的随机种子
            for i in range(5):
                params.randomSeed = seed + i + 1
                if AllChem.EmbedMolecule(mol, params) == 0:
                    break
            else:
                return None
        try:
            AllChem.MMFFOptimizeMolecule(mol)
        except Exception:
            pass
        return mol
    except Exception:
        return None


def rdkit_mol_to_xyz(mol: "Mol") -> Optional[str]:
    """将RDKit分子导出为XYZ字符串（单位Å）。"""
    if not HAS_RDKIT or mol is None:
        return None
    try:
        conf = mol.GetConformer()
        natoms = mol.GetNumAtoms()
        lines = []
        lines.append(str(natoms))
        lines.append("generated by dft_utils")
        pt = Chem.GetPeriodicTable()
        for idx in range(natoms):
            atom = mol.GetAtomWithIdx(idx)
            sym = atom.GetSymbol()
            pos = conf.GetAtomPosition(idx)
            lines.append(f"{sym} {pos.x:.6f} {pos.y:.6f} {pos.z:.6f}")
        return "\n".join(lines) + "\n"
    except Exception:
        return None


# -------------------------
# Psi4 计算
# -------------------------

def _run_psi4_single_point(xyz: str, charge: int = 0, multiplicity: int = 1,
                           method: str = "wb97x-d", basis: str = "def2-svp",
                           pcm: bool = True) -> Dict[str, Any]:
    """使用 Psi4 进行单点计算，返回电子结构信息。"""
    if not HAS_PSI4:
        return {"success": False, "message": "psi4 not installed"}
    try:
        import psi4  # type: ignore
        psi4.core.be_quiet()
        mol_str = [
            "molecule mol {",
            f"  {charge} {multiplicity}",
        ]
        # 将xyz部分转换为 psi4 geometry 坐标节
        xyz_lines = xyz.strip().splitlines()
        # 跳过前两行（xyz格式的头和注释）
        for line in xyz_lines[2:]:
            parts = line.split()
            if len(parts) >= 4:
                sym, x, y, z = parts[:4]
                mol_str.append(f"  {sym} {x} {y} {z}")
        mol_str.extend([
            "  units angstrom",
            "  no_com",
            "  no_reorient",
            "  symmetry c1",
            "}",
        ])
        geom = "\n".join(mol_str)
        mol = psi4.geometry(geom)

        options = {
            "basis": basis,
            "scf_type": "df",
            "d_convergence": 1e-8,
            "geom_maxiter": 200,
        }
        if pcm:
            options.update({"pcm": True, "pcm_scf_type": "total"})
            psi4.set_options({
                "pcm": True,
                "pcm_scf_type": "total",
            })
            pcm_str = """
            pcm = True
            pcm_scf_type = total
            """
            psi4.set_options(options)
        else:
            psi4.set_options(options)

        e, wfn = psi4.energy(f"{method}/{basis}", return_wfn=True)
        energy_hartree = float(e)

        # 解析轨道能量（单位 Hartree）
        homo_ev = None
        lumo_ev = None
        gap_ev = None
        eta_ev = None
        dipole_debye = None
        try:
            eps_a = np.array(wfn.epsilon_a().to_array()) if HAS_NUMPY else None
            nalpha = int(wfn.nalpha())
            if eps_a is not None and eps_a.size > nalpha:
                ehomo = float(eps_a[nalpha - 1])
                elumo = float(eps_a[nalpha])
                homo_ev = ehomo * HARTREE_TO_EV
                lumo_ev = elumo * HARTREE_TO_EV
                gap_ev = (elumo - ehomo) * HARTREE_TO_EV
                eta_ev = gap_ev / 2.0
        except Exception:
            pass
        try:
            # Psi4 报告的偶极（Debye）
            mux = float(wfn.variable("SCF DIPOLE X"))
            muy = float(wfn.variable("SCF DIPOLE Y"))
            muz = float(wfn.variable("SCF DIPOLE Z"))
            dipole_debye = float(math.sqrt(mux * mux + muy * muy + muz * muz))
        except Exception:
            pass

        return {
            "success": True,
            "engine": "psi4",
            "energy_hartree": energy_hartree,
            "homo_ev": homo_ev,
            "lumo_ev": lumo_ev,
            "gap_ev": gap_ev,
            "eta_ev": eta_ev,
            "dipole_debye": dipole_debye,
            "message": None,
        }
    except Exception as ex:
        return {"success": False, "engine": "psi4", "message": str(ex)}


# -------------------------
# xTB 计算（CLI）
# -------------------------

def _run_xtb_single_point(xyz: str, workdir: str) -> Dict[str, Any]:
    """调用 xTB CLI 进行单点性质计算，解析 gap(Ev) 与总能量。"""
    if not HAS_XTB:
        return {"success": False, "message": "xtb not found"}
    try:
        os.makedirs(workdir, exist_ok=True)
        in_xyz = os.path.join(workdir, "mol.xyz")
        with open(in_xyz, "w", encoding="utf-8") as f:
            f.write(xyz)
        # 只做单点性质，避免长优化
        cmd = [XTB_EXE, in_xyz, "--gfn", "2", "--sp"]
        proc = subprocess.run(cmd, cwd=workdir, stdout=subprocess.PIPE, stderr=subprocess.STDOUT, text=True)
        out = proc.stdout
        energy_hartree = None
        gap_ev = None
        homo_ev = None
        lumo_ev = None
        dipole_debye = None

        # 解析输出
        for line in out.splitlines():
            s = line.strip()
            if "HOMO-LUMO GAP" in s.upper() and "EV" in s.upper():
                # 可能格式:  "HOMO-LUMO GAP       :    3.456 eV"
                try:
                    parts = s.replace(":", " ").replace("=", " ").split()
                    vals = [p for p in parts if _is_float(p)]
                    if vals:
                        gap_ev = float(vals[-1])
                except Exception:
                    pass
            if "TOTAL ENERGY" in s.upper() and "EH" in s.upper():
                # 可能格式:  "TOTAL ENERGY       -40.123456 Eh"
                try:
                    parts = s.replace(":", " ").replace("=", " ").split()
                    vals = [p for p in parts if _is_float(p)]
                    if vals:
                        energy_hartree = float(vals[-1])
                except Exception:
                    pass
            if "DIPOLE MOMENT" in s.upper() and "DEBYE" in s.upper():
                # 可能格式: "DIPOLE MOMENT:   1.234 Debye"
                try:
                    parts = s.replace(":", " ").split()
                    vals = [p for p in parts if _is_float(p)]
                    if vals:
                        dipole_debye = float(vals[-1])
                except Exception:
                    pass
        eta_ev = gap_ev / 2.0 if gap_ev is not None else None
        return {
            "success": True,
            "engine": "xtb",
            "energy_hartree": energy_hartree,
            "homo_ev": homo_ev,
            "lumo_ev": lumo_ev,
            "gap_ev": gap_ev,
            "eta_ev": eta_ev,
            "dipole_debye": dipole_debye,
            "message": None,
        }
    except Exception as ex:
        return {"success": False, "engine": "xtb", "message": str(ex)}


def _is_float(x: str) -> bool:
    try:
        float(x)
        return True
    except Exception:
        return False


# -------------------------
# 主入口：对单个SMILES计算电子结构
# -------------------------

def compute_dft_for_smiles(
    smiles: str,
    workdir: str,
    prefer: str = "psi4",
    charge: int = 0,
    multiplicity: int = 1,
) -> Dict[str, Any]:
    """对一个SMILES计算电子结构，优先使用Psi4；不可用时退化到xTB；都不可用时返回失败。"""
    os.makedirs(workdir, exist_ok=True)

    res: Dict[str, Any] = {
        "success": False,
        "engine": None,
        "smiles": smiles,
        "charge": charge,
        "multiplicity": multiplicity,
        "energy_hartree": None,
        "homo_ev": None,
        "lumo_ev": None,
        "gap_ev": None,
        "eta_ev": None,
        "dipole_debye": None,
        "message": None,
    }

    if not HAS_RDKIT:
        res["message"] = "rdkit not installed"
        return res

    mol = smiles_to_3d_mol(smiles)
    if mol is None:
        res["message"] = "failed to build 3D conformer"
        return res

    xyz = rdkit_mol_to_xyz(mol)
    if xyz is None:
        res["message"] = "failed to export xyz"
        return res

    # Psi4优先
    if prefer.lower() == "psi4" and HAS_PSI4:
        psi4_res = _run_psi4_single_point(xyz, charge=charge, multiplicity=multiplicity)
        if psi4_res.get("success"):
            res.update(psi4_res)
            return res
        else:
            # 降级到xTB
            if HAS_XTB:
                xtb_res = _run_xtb_single_point(xyz, workdir=workdir)
                res.update(xtb_res)
                return res
            res.update(psi4_res)
            return res

    # 若首选不是psi4或psi4不可用，则使用xTB
    if HAS_XTB:
        xtb_res = _run_xtb_single_point(xyz, workdir=workdir)
        res.update(xtb_res)
        return res

    # 全部不可用
    res["message"] = "no dft engine available (psi4/xtb missing)"
    return res


